package com.techzealot.tvm.examples.primitives;

public class PrintBasic {

    public static void main(String[] args) {
        boolean v = true;
        byte b = -12;
        char c = 69;
        short s = -10;

        System.out.println(v);
        System.out.println(b);
        System.out.println(c);
        System.out.println(s);
    }
}
